/*
Inheritance creates a new class dervied from an existing class. 
The new class is called a derived class and the existing class is called a base class. 
The derived class inherits all the members of the base class, 
but it can also add new members and override existing members. 
The derived class can also access the members of the base class.
*/

/*
Single inheritance:
derived class has a single base class


Multiple inheritance:
derived class has multiple base classes
*/

